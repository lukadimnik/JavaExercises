public class WordPair {

	private String englishWord;
	private String finnishhWord;

	public WordPair(String english, String finnish) {

		englishWord = english;
		finnishhWord = finnish;

	}

	public String getEnglishWord() {
		return englishWord;

	}

	public String getFinnishWord() {
		return finnishhWord;

	}

}